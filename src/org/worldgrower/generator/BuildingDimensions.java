package org.worldgrower.generator;

public enum BuildingDimensions {
	ARENA(11, 10),
	JAIL(3, 4),
	SHACK(3, 4);
	
	private final int width;
	private final int height;
	
	private BuildingDimensions(int width, int height) {
		this.width = width;
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}
}
